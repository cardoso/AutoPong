/*:
 > This playground uses SpriteKit, all the documentation for SpriteKit applies!
 
 
 Below is a list of identifiers that can be used inside the exercises and in Page 4.3

 
 # General
   * **gameVC** - the ViewController
   * **scene** - the SKScene
   * **update** - a function receiving a closure that runs every frame
 
 # The paddles
   * **topPaddle** - the PaddleNode at the top
   * **rightPaddle** - the PaddleNode at the right
   * **bottomPaddle** - the PaddleNode at the bottom
   * **leftPaddle** - the PaddleNode at the left
 
 # PaddleNode
   * **PaddleNode.kick()** - perform a kick
   * **PaddleNode.moveRight(by: Double)** - move the paddle right
   * **PaddleNode.moveLeft(by: Double)** - move the paddle left
   * **PaddleNode.onScore** - a closure that is invoked when the paddle scores
   * **PaddleNode.onScored** - a closure that is invoked when the paddle is scored
 
 2. Balls
   * **balls** - an array of the spawned balls
   * **spawnBalls(quantity: UInt)** - spawn one or multiple balls
 */
